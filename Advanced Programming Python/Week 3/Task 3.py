# Функция для сортировки букв в каждом слове строки
def sort_letters_in_words(text):
    # Разбиваем строку на слова
    words = text.split()

    # Список для новых слов
    result = []

    for word in words:
        # Сортируем буквы в слове и объединяем обратно в строку
        sorted_word = ''.join(sorted(word))
        result.append(sorted_word)

    # Объединяем слова обратно в строку
    return ' '.join(result)


