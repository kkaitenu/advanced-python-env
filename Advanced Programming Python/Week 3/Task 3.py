import math

# Функция для вычисления гипотенузы прямоугольного треугольника
def hypotenuse(a, b):
    # Теорема Пифагора: c = √(a² + b²)
    return math.sqrt(a * a + b * b)


# Ввод катетов первого треугольника
a1 = float(input("Введите первый катет первого треугольника: "))
b1 = float(input("Введите второй катет первого треугольника: "))

# Ввод катетов второго треугольника
a2 = float(input("\nВведите первый катет второго треугольника: "))
b2 = float(input("Введите второй катет второго треугольника: "))

# Вычисление гипотенуз
c1 = hypotenuse(a1, b1)
c2 = hypotenuse(a2, b2)

# Вывод результатов
print("\nГипотенуза первого треугольника:", c1)
print("Гипотенуза второго треугольника:", c2)

# Сравнение гипотенуз
if c1 > c2:
    print("Гипотенуза первого треугольника больше")
elif c1 < c2:
    print("Гипотенуза второго треугольника больше")
else:
    print("Гипотенузы равны")

