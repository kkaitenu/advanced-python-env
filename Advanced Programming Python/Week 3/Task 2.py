import math

# Функция для вычисления площади равностороннего треугольника
def triangle_area(a):
    # Формула площади равностороннего треугольника: (√3 / 4) * a²
    return (math.sqrt(3) / 4) * a * a


# Функция для вычисления площади правильного шестиугольника
def hexagon_area(a):
    # Правильный шестиугольник состоит из 6 равносторонних треугольников
    return 6 * triangle_area(a)


# Ввод стороны шестиугольника
a = float(input("Введите сторону шестиугольника: "))

# Вывод площади шестиугольника
print("Площадь правильного шестиугольника:", hexagon_area(a))

