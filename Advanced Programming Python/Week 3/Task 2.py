# Функция для вычисления площади прямоугольника
def rectangle_area(a, b):
    # Площадь прямоугольника равна произведению его сторон
    return a * b


# Цикл для трёх прямоугольников
for i in range(1, 4):
    print(f"\nПрямоугольник {i}")

    # Ввод сторон прямоугольника
    a = float(input("Введите первую сторону: "))
    b = float(input("Введите вторую сторону: "))

    # Вычисление и вывод площади
    print("Площадь =", rectangle_area(a, b))

